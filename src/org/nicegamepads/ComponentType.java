package org.nicegamepads;

/**
 * Enumeration of possible component types.  Based upon jinput.
 * 
 * @author Andrew Hayden
 */
public enum ComponentType
{
    /**
     * A key.
     */
    KEY,

    /**
     * A button.
     */
    BUTTON,

    /**
     * An axis.
     */
    AXIS;
}